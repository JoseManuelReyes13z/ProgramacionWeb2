var numUsu = parseInt(prompt("Escoja la opcion del dia de la semana que desee\nLunes - 1 \nMartes - 2\nMiercoles - 3\nJueves - 4\nViernes - 5"));
switch(numUsu){
    case 1:
        console.log("Lunes");
        break;;
    case 2:
        console.log("Martes");
        break;
    case 3:
        console.log("Miercoles");
        break;
    case 4:
        console.log("Juebebes");
        break;
    case 5:
        console.log("Viernes");
        break;
    default:
        console.log("Numero invalido");
}